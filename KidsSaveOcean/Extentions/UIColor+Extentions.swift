import UIKit

extension UIColor {
    
    class var backgroundGray: UIColor {
        return UIColor(red: 245 / 255, green: 245 / 255, blue: 245 / 255, alpha: 1.0)
    }
    
    
    static var random: UIColor {
        return UIColor(red: .random, green: .random, blue: .random, alpha: 1.0)
    }
        
    
}
